import madukaLogo from "../assets/maduka-logo.png";

export const addTranscriptHeader = (doc) => {
  // Add Logo
  doc.addImage(madukaLogo, "PNG", 85, 10, 40, 40);

  // University Name
  doc.setFontSize(14);
  doc.setFont("helvetica", "bold");
  doc.text("MADUKA UNIVERSITY, EKWGEBE ENUGU STATE", 105, 60, { align: "center" });

  // Contact Info
  doc.setFontSize(11);
  doc.setFont("helvetica", "normal");
  doc.text("Email: vc@madukauniversity.edu.ng", 105, 70, { align: "center" });
  doc.text("Website: madukauniversity.edu.ng", 105, 78, { align: "center" });

  // Transcript Title
  doc.setFontSize(13);
  doc.setFont("helvetica", "bold");
  doc.text("STUDENT TRANSCRIPT", 105, 90, { align: "center" });
};

export const addTranscriptFooter = (doc) => {
  const pageHeight = doc.internal.pageSize.height;
  const today = new Date().toLocaleDateString();

  doc.setFontSize(9);
  doc.setFont("helvetica", "italic");
  doc.text(
    `Generated by Maduka University Result Portal | ${today}`,
    105,
    pageHeight - 10,
    { align: "center" }
  );
};
